function getPracticesByDirection(directionId) {
  return PRACTICES.filter(p => p.direction === directionId);
}

function getPracticeById(id) {
  return PRACTICES.find(p => p.id === parseInt(id));
}

function getQuestionsByDirection(directionId) {
  return QUESTIONS.filter(q => q.direction === directionId);
}

function shuffleArray(array) {
  const arr = [...array];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}
